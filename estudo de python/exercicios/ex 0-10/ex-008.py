

metros = float(input("Digite a medida em metros:"))
km = metros/1000
hm = metros/100
dam = metros/10
dm = metros*10
cm = metros*100
mm = metros*1000
print("a medida em kilometros é {:.3f}".format(km))
print("a medida em hectômetros é {:.2f}".format(hm))
print("a medida em decâmetros é {:.2f}".format(dam))
print("a medida em decímetros é {:.2f}".format(dm))
print("a medida em centímetros é {:.2f}".format(cm))
print("a medida em milimetros é {:.2f}".format(mm))
